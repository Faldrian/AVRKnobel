void readString(const char* text, uint16_t length) {
	if(phase == 0) {
		// hole das passende Byte aus dem Speicher
		int buchstabe = (int) pgm_read_byte (&text[strpos]);

		// passendes bit holen
		tmp = buchstabe & (1 << (7 - bitpos)); // inverted bits

		// bit im ausgang entsprechend setzen
        if(tmp == 0) {
			PORTD &= ~(1<<PD7);
		} else {
			PORTD |= (1<<PD7);
		}

		// inkrement die Werte
		bitpos++;
		if(bitpos == 8) { // √úberlauf, n√§chster Buchstabe
			strpos++;
			if(strpos == length) { // √úberlauf, fange von vorn an
				strpos = 0;
			}
			bitpos = 0;
		}

		// Schalte die clock auf 1
		PORTD |= (1<<PD6);
	} else {
		// Setze die clock wieder auf 0
		PORTD &= ~(1<<PD6);
	}

	phase ^= 1; // 1 -> 0 -> 1 ...
}

void morseString(const char* text, uint16_t length) {
	if(morsecounter == morse_tempo) {
		// Schalte die LED aus, damit wir eine Pause zum n√§chsten Zeichen haben
		PORTD &= ~(1<<PD7);
	}

    if(morsecounter == 0) {
		// hole das passende Byte aus dem Speicher
		uint8_t buchstabe = (uint8_t) pgm_read_byte (&text[morsepos]);

		// passendes bit holen
		if(buchstabe == (uint8_t)'-') {
			PORTD |= (1<<PD7);
			morsecounter = 3 * morse_tempo + morse_tempo; // dah
		} else if(buchstabe == (uint8_t)'.') {
			PORTD |= (1<<PD7);
			morsecounter =     morse_tempo + morse_tempo; // dit
		} else if(buchstabe == (uint8_t)'/') {
			morsecounter = 5 * morse_tempo + morse_tempo; // pause zwischen w√∂rtern
		} else {
			morsecounter = 2 * morse_tempo + morse_tempo; // pause zwischen Zeichen
		}

		// inkrement die Werte
		morsepos++;
		if(morsepos == length) { // √úberlauf, fange von vorn an
			morsepos = 0;
		}
	}
    morsecounter--;
}

